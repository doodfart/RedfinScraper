public class house {

    public int cost;
    public int bed;
    public float bath;
    public int sqft;
    public String address;
    public String houseURL;

    public house(int cost, int bed, float bath, int sqft,
                 String address, String houseURL) {
        this.cost = cost;
        this.bed = bed;
        this.bath = bath;
        this.sqft = sqft;
        this.address = address;
        this.houseURL = houseURL;
    }
}